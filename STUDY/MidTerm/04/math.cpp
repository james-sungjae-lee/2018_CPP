#include <iostream>
#include <cmath>
using namespace std;

int main(int argc, char const *argv[]) {
  double pi = 3.1415926535;
  std::cout << sin(pi / 2.0) << '\n';
  std::cout << cos(pi / 2.0) << '\n';
  std::cout << tan(0.5) << '\n';
  std::cout << log(10.0) << '\n';
  std::cout << log10(100.0) << '\n';
  std::cout << exp(10.0) << '\n';
  return 0;
}
